# 在Python中所有可以自主命名的都属于标识符
# 如：变量名、函数名、类名
# 标识符的规范
#	1.标识符中含有字母、数字、_，不能以数字开头
#	2.标识符不能是Python中的关键字和保留字，不建议使用Python的函数名作为标识符，这样会导致函数被覆盖
#	3.严格区分大小写
#   3.命名规范
# 		a)下划线命名法：所有字母小写，单词之间使用_分割
#			max_length min_length xxx_yyy_zzz
# 		b)帕斯卡命名法（大驼峰命名法）：首字母大写，每个单词开头大写，其他小写
# 			MaxLength MinLength XxxYyyZzz
# 		一般类名用帕斯卡，其他都是用下划线命名法


